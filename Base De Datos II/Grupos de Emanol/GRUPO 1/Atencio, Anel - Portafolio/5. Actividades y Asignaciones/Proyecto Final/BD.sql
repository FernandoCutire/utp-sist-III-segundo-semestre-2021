--  DDL for View PROV_DOM
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PF_G1"."PROV_DOM" ("NUM_DOMIC", "NOMBRE_CORREG", "NOMBRE_PROV", "COD_PROV") AS 
  select d.num_domic, c.nombre_correg, p.nombre_prov,p.Cod_prov
from Domicilio d join Corregimiento c
on d.Cod_correg=c.Cod_correg join Distrito dis
on dis.Cod_dist=c.Cod_dist join Provincia p
on p.Cod_prov=dis.Cod_prov
;
--------------------------------------------------------
--  DDL for View PROV_PER
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PF_G1"."PROV_PER" ("APELLIDO_P", "NOMBRE_P", "NOMBRE_PROV") AS 
  select per.apellido_p, nombre_p, pro.nombre_prov
from Provincia pro join Distrito dis
on pro.Cod_prov = dis.Cod_prov join Corregimiento cor
on dis.Cod_dist = cor.Cod_dist join Domicilio dom
on cor.Cod_correg = dom.Cod_correg join Persona per
on per.Num_domic = dom.Num_domic
where pro.Cod_prov between 1 and 5
;
--------------------------------------------------------
--  DDL for View PROV_PER_DOM
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PF_G1"."PROV_PER_DOM" ("APELLIDO_P", "NOMBRE_P") AS 
  select per.apellido_p, nombre_p
from Provincia pro join Distrito dis
on pro.Cod_prov = dis.Cod_prov join Corregimiento cor
on dis.Cod_dist = cor.Cod_dist join Domicilio dom
on cor.Cod_correg = dom.Cod_correg join Persona per
on per.Num_domic = dom.Num_domic join Persona_Sintoma pers
on per.id_persona = pers.id_persona join Persona_Enfermedades_Previas peref
on pers.id_persona = peref.id_persona
where pro.Cod_prov = 8 and pers.Cod_sint not in ('F', 'TS') or peref.Cod_enf = 'DIA'
;
