OPEN c_edad;
LOOP
FETCH c_edad INTO v_idp, v_cedula, v_nombre, v_apellido, v_edad;
v_funcion:=population(v_edad);
INSERT INTO per_stat (numstad, id_persona, cedula, nombre, apellido, edad, estado)
