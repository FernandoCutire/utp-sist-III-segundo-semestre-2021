EXIT WHEN c_edad%NOTFOUND;
END LOOP;
CLOSE c_edad;
EXCEPTION
WHEN NO_DATA_FOUND THEN
     DBMS_OUTPUT.PUT_LINE ('No se encontr¢ los datos');
