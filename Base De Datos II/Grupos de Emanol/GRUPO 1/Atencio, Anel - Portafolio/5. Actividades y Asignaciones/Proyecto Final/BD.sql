--  DDL for View SIN
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "PF_G1"."SIN" ("APELLIDO_P", "NOMBRE_P", "NOMBRE_SINT", "CEDULA_P") AS 
